import type { 网络机柜规格, 是或否 } from "@/types/设备";

const 常用U数 = [6, 9, 12, 18, 22, 27, 32, 42];
const 常用宽度 = [600, 800];
const 常用深度 = [450, 600, 800, 1000];
const 常用机柜高度毫米: Record<number, number> = {
  6: 350,
  9: 500,
  12: 600,
  18: 1000,
  22: 1200,
  27: 1400,
  32: 1600,
  42: 2000
};

function 估算机柜高度毫米(机柜U数: number) {
  if (常用机柜高度毫米[机柜U数]) return 常用机柜高度毫米[机柜U数];
  const 每U高度 = 44.45;
  const 顶底结构余量 = 120;
  return Math.round(机柜U数 * 每U高度 + 顶底结构余量);
}

function 是否壁挂(机柜U数: number, 深度毫米: number): 是或否 {
  return 机柜U数 <= 12 && 深度毫米 <= 600 ? "是" : "否";
}

function 是否落地(机柜U数: number, 深度毫米: number): 是或否 {
  return 机柜U数 >= 18 || 深度毫米 >= 800 ? "是" : "否";
}

function 推荐承重(机柜U数: number, 深度毫米: number) {
  if (机柜U数 <= 9) return 深度毫米 >= 600 ? 45 : 35;
  if (机柜U数 <= 12) return 深度毫米 >= 600 ? 60 : 45;
  if (机柜U数 <= 22) return 深度毫米 >= 800 ? 160 : 120;
  if (机柜U数 <= 32) return 深度毫米 >= 800 ? 260 : 200;
  return 深度毫米 >= 800 ? 500 : 350;
}

function 推荐用途(机柜U数: number, 宽度毫米: number, 深度毫米: number) {
  if (机柜U数 <= 9) return "别墅弱电箱扩展、小型网络机柜、光猫与路由集中安装";
  if (机柜U数 <= 12) return "别墅弱电机房、安防小系统、少量交换机与NVR";
  if (机柜U数 <= 22) return "影音机柜、安防机柜、中小型网络汇聚";
  if (机柜U数 <= 32) return "别墅弱电机房主机柜、NAS与UPS集中安装";
  return 宽度毫米 === 800 || 深度毫米 >= 1000 ? "大型弱电机房、影音与安防综合机柜" : "标准网络机房主机柜";
}

export const 网络机柜规格库: 网络机柜规格[] = 常用U数.flatMap((机柜U数) =>
  常用宽度.flatMap((宽度毫米) =>
    常用深度.map((深度毫米): 网络机柜规格 => {
      const 高度毫米 = 估算机柜高度毫米(机柜U数);
      const 壁挂 = 是否壁挂(机柜U数, 深度毫米);
      const 落地 = 是否落地(机柜U数, 深度毫米);
      return {
        规格编号: `机柜-${机柜U数}U-${宽度毫米}-${深度毫米}`,
        规格名称: `${机柜U数}U ${宽度毫米}×${深度毫米}×${高度毫米}mm 网络机柜`,
        机柜U数,
        宽度毫米,
        深度毫米,
        高度毫米,
        是否壁挂机柜: 壁挂,
        是否落地机柜: 落地,
        推荐最大承重千克: 推荐承重(机柜U数, 深度毫米),
        推荐用途: 推荐用途(机柜U数, 宽度毫米, 深度毫米),
        备注:
          壁挂 === "是"
            ? "适合壁挂或小型落地支架，需按现场墙体承重确认"
            : 落地 === "是"
              ? "适合落地安装，预留前后维护通道和底部进线"
              : "适合轻量网络设备集中安装"
      };
    })
  )
);
