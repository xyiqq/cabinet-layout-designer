export function 设备是否支持数量调整(设备: 设备参数) {
  return 设备.是否支持数量调整 === "是";
}

export function 计算设备占用宽度(设备: 设备参数, 数量 = 1) {
  if (!设备是否支持数量调整(设备)) return 设备.宽度毫米;
  const 单个宽度 = 设备.单个宽度毫米 ?? 设备.宽度毫米;
  return Math.max(单个宽度, 单个宽度 * Math.max(1, 数量));
}

export function 获取实例数量(项目: 已放置设备) {
  if (设备是否支持数量调整(项目.设备) && typeof 项目.行数 === "number" && typeof 项目.列数 === "number") {
    return Math.max(1, Math.round(项目.行数) * Math.round(项目.列数));
  }
  return Math.max(1, Math.round(项目.数量 ?? 1));
}

export function 获取实例行数(项目: 已放置设备) {
  if (!设备是否支持数量调整(项目.设备)) return 1;
  return Math.max(1, Math.round(项目.行数 ?? 1));
}

export function 获取实例列数(项目: 已放置设备) {
  if (!设备是否支持数量调整(项目.设备)) return 1;
  const 数量 = 获取实例数量(项目);
  return Math.max(1, Math.round(项目.列数 ?? 数量));
}

export function 计算实例占用宽度(项目: 已放置设备) {
  return 计算设备占用宽度(项目.设备, 获取实例列数(项目));
}

export function 计算设备显示高度(设备: 设备参数) {
  return Math.max(36, Math.min(160, 设备.高度毫米));
}

export function 计算实例显示高度(项目: 已放置设备) {
  return 计算设备显示高度(项目.设备) * 获取实例行数(项目);
}

export function 设备是否导轨安装(设备: 设备参数) {
  return 设备.安装方式 === "DIN导轨";
}

function 设备是否空开端子区设备(设备: 设备参数) {
  return (
    设备.安装方式 === "端子排" ||
    设备.设备类别.includes("端子排") ||
    设备.设备类别.includes("保护") ||
    设备.设备类型.includes("断路器") ||
    设备.设备类型.includes("空开") ||
    设备.设备类型.includes("漏电保护") ||
    设备.设备名称.includes("空气开关") ||
    设备.设备名称.includes("漏电保护")
  );
}

export function 获取设备布局分区名称(设备: 设备参数) {
  if (设备是否空开端子区设备(设备)) return "空开 / 端子接线区";
  if (设备.设备类别.includes("电源")) return "电源模块区";
  if (设备.是否强电设备 === "是" && 设备.是否弱电设备 === "否") return "强电模块区";
  if (设备.是否强电设备 === "是" && 设备.是否弱电设备 === "是") return "强弱电混合模块区";
  if (设备.是否弱电设备 === "是") return "弱电通讯模块区";
  return "通用模块区";
}

export function 计算导轨层空间高度(布局规则: 布局规则, 分区类型 = "") {
  return 分区类型.includes("空开") || 分区类型.includes("端子")
    ? 布局规则.空开端子区高度
    : 布局规则.模块区高度;
}

function 计算导轨中心线距行顶(布局规则: 布局规则, 分区类型 = "") {
  const 分区高度 = 计算导轨层空间高度(布局规则, 分区类型);
  return Math.round(Math.max(54, Math.min(120, 分区高度 * 导轨中心线距行顶比例)));
}

export function 计算导轨层顶部Y(
  布局规则: 布局规则,
  层索引: number,
  分区类型列表: string[] = [],
  默认分区类型 = ""
) {
  let y = 布局规则.顶部预留空间;
  for (let 索引 = 0; 索引 < 层索引; 索引 += 1) {
    y += 计算导轨层空间高度(布局规则, 分区类型列表[索引] ?? 默认分区类型);
    y += 布局规则.横向线槽高度;
  }
  return y;
}

export function 计算导轨中心Y(
  布局规则: 布局规则,
  层索引: number,
  分区类型列表: string[] = [],
  默认分区类型 = ""
) {
  const 分区类型 = 分区类型列表[层索引] ?? 默认分区类型;
  return 计算导轨层顶部Y(布局规则, 层索引, 分区类型列表, 默认分区类型) + 计算导轨中心线距行顶(布局规则, 分区类型);
}

export function 计算导轨安装顶部Y(设备: 设备参数, 布局规则: 布局规则, 层索引: number, 分区类型列表: string[] = []) {
  return Math.round(
    计算导轨中心Y(布局规则, 层索引, 分区类型列表, 获取设备布局分区名称(设备)) - 计算设备显示高度(设备) / 2
  );
}

export function 计算最近导轨安装顶部Y(
  设备: 设备参数,
  位置Y毫米: number,
  导轨数量: number,
  布局规则: 布局规则,
  分区类型列表: string[] = []
) {
  const 有效导轨数量 = Math.max(1, 导轨数量);
  const 设备中心Y = 位置Y毫米 + 计算设备显示高度(设备) / 2;
  let 最近层索引 = 0;
  let 最近距离 = Number.POSITIVE_INFINITY;

  for (let 索引 = 0; 索引 < 有效导轨数量; 索引 += 1) {
    const 距离 = Math.abs(计算导轨中心Y(布局规则, 索引, 分区类型列表, 获取设备布局分区名称(设备)) - 设备中心Y);
    if (距离 < 最近距离) {
      最近距离 = 距离;
      最近层索引 = 索引;
    }
  }

  return 计算导轨安装顶部Y(设备, 布局规则, 最近层索引, 分区类型列表);
}

function 矩形是否相交(
  甲: { x: number; y: number; width: number; height: number },
  乙: { x: number; y: number; width: number; height: number },
  间距毫米: number
) {
  return (
    甲.x < 乙.x + 乙.width + 间距毫米 &&
    甲.x + 甲.width + 间距毫米 > 乙.x &&
    甲.y < 乙.y + 乙.height + 间距毫米 &&
    甲.y + 甲.height + 间距毫米 > 乙.y
  );
}

function 限制数值(值: number, 最小值: number, 最大值: number) {
  return Math.max(最小值, Math.min(最大值, 值));
}

export function 查找设备加入空位(设备列表: 已放置设备[], 新设备: 已放置设备, 计算结果: 计算结果, 箱体配置: 箱体配置) {
  const 布局规则 = 合并布局规则(箱体配置);
  const 箱体 = 计算结果.推荐箱体尺寸;
  const 分区类型列表 = 计算结果.每层设备分布.map((层) => 层.分区类型);
  const 新宽度 = 计算实例占用宽度(新设备);
  const 新高度 = 计算实例显示高度(新设备);
  if (新宽度 > 箱体.宽度毫米 || 新高度 > 箱体.高度毫米 || 新设备.设备.深度毫米 > 箱体.深度毫米) {
    return null;
  }

  const 最大X = Math.max(0, 箱体.宽度毫米 - 新宽度);
  const 最大Y = Math.max(0, 箱体.高度毫米 - 新高度);
  const 左起点 = 布局规则.左侧竖向线槽宽度 + 24;
  const 导轨右 = 箱体.宽度毫米 - 布局规则.右侧竖向线槽宽度 - 20;
  const 间距毫米 = 0;
  const 候选位置 = new Map<string, { x: number; y: number }>();

  const 加候选 = (x: number, y: number) => {
    const 锁定Y = 设备是否导轨安装(新设备.设备)
      ? 计算最近导轨安装顶部Y(新设备.设备, y, 计算结果.DIN导轨数量 + 2, 布局规则, 分区类型列表)
      : y;
    const 候选X = Math.round(限制数值(x, 0, 最大X));
    const 候选Y = Math.round(限制数值(锁定Y, 0, 最大Y));
    候选位置.set(`${候选X},${候选Y}`, { x: 候选X, y: 候选Y });
  };

  if (新设备.设备.安装方式 === "端子排") {
    加候选(
      左起点,
      Math.max(
        布局规则.顶部预留空间,
        箱体.高度毫米 - 布局规则.底部预留空间 - 布局规则.端子排区域高度 + 28
      )
    );
  }

  for (let 索引 = 0; 索引 < 计算结果.DIN导轨数量 + 2; 索引 += 1) {
    const 导轨Y = 计算导轨中心Y(布局规则, 索引, 分区类型列表, 获取设备布局分区名称(新设备.设备));
    if (导轨Y > 箱体.高度毫米 - 布局规则.底部预留空间 + 新高度) continue;
    加候选(左起点, 导轨Y - 新高度 / 2);
    加候选(Math.max(左起点, 导轨右 - 新宽度), 导轨Y - 新高度 / 2);
  }

  for (const 项目 of 设备列表) {
    const 宽度 = 计算实例占用宽度(项目);
    const 高度 = 计算实例显示高度(项目);
    const 右侧 = 项目.位置X毫米 + 宽度 + 间距毫米;
    const 左侧 = 项目.位置X毫米 - 新宽度 - 间距毫米;
    const 下方 = 项目.位置Y毫米 + 高度 + 间距毫米;
    const 上方 = 项目.位置Y毫米 - 新高度 - 间距毫米;

    加候选(右侧, 项目.位置Y毫米);
    加候选(左侧, 项目.位置Y毫米);
    加候选(项目.位置X毫米, 下方);
    加候选(项目.位置X毫米, 上方);
    加候选(右侧, 下方);
    加候选(左侧, 下方);
  }

  const 扫描步长 = 10;
  for (let y = 布局规则.顶部预留空间; y <= 最大Y; y += 扫描步长) {
    for (let x = 左起点; x <= 最大X; x += 扫描步长) {
      加候选(x, y);
    }
  }

  const 是否重叠 = (x: number, y: number) => {
    const 当前矩形 = { x, y, width: 新宽度, height: 新高度 };
    return 设备列表.some((项目) => {
      const 其他矩形 = {
        x: 项目.位置X毫米,
        y: 项目.位置Y毫米,
        width: 计算实例占用宽度(项目),
        height: 计算实例显示高度(项目)
      };
      return 矩形是否相交(当前矩形, 其他矩形, 间距毫米);
    });
  };

  const 起点X = 新设备.位置X毫米;
  const 起点Y = 新设备.位置Y毫米;
  return (
    Array.from(候选位置.values())
      .sort((甲, 乙) => Math.hypot(甲.x - 起点X, 甲.y - 起点Y) - Math.hypot(乙.x - 起点X, 乙.y - 起点Y))
      .find((候选) => !是否重叠(候选.x, 候选.y)) ?? null
  );
}

function 合并布局规则(箱体配置?: 箱体配置): 布局规则 {
  return {
    ...默认布局规则,
    ...(箱体配置?.布局规则 ?? {})
  };
}

function 选择候选规格(候选规格: number[], 需求值: number) {
  return 候选规格.find((规格) => 规格 >= 需求值) ?? 候选规格[候选规格.length - 1];
}

function 格式化尺寸(尺寸: 箱体尺寸) {
  return `${尺寸.宽度毫米} × ${尺寸.高度毫米} × ${尺寸.深度毫米} 毫米`;
}

function 设备排序权重(设备: 设备参数) {
  if (设备是否空开端子区设备(设备)) return 10;
  if (设备.是否重设备 === "是") return 80;
  if (设备.设备类别.includes("电源")) return 70;
  if (设备.是否强电设备 === "是" && 设备.是否弱电设备 === "否") return 20;
  if (设备.是否强电设备 === "是" && 设备.是否弱电设备 === "是") return 30;
  if (设备.设备类别.includes("PLC") || 设备.设备类别.includes("KNX")) return 40;
  if (设备.是否弱电设备 === "是") return 50;
  return 60;
}

function 分区名称(设备: 设备参数) {
  return 获取设备布局分区名称(设备);
}

export function 创建设备实例(设备: 设备参数, 位置X毫米 = 90, 位置Y毫米 = 110, 数量?: number): 已放置设备 {
  const 初始数量 = Math.max(1, Math.round(数量 ?? 设备.默认数量 ?? 1));
  return {
    实例编号: `${设备.设备编号}-${Date.now()}-${Math.round(Math.random() * 10000)}`,
    设备,
    数量: 初始数量,
    行数: 设备是否支持数量调整(设备) ? 1 : undefined,
    列数: 设备是否支持数量调整(设备) ? 初始数量 : undefined,
    位置X毫米,
    位置Y毫米,
    所在层: 0,
    布局来源: "手动拖拽"
  };
}

function 规划导轨层(设备列表: 已放置设备[], 每条导轨可用宽度: number, 布局规则: 布局规则): 导轨层[] {
  const 可排布宽度 = Math.max(120, 每条导轨可用宽度 * (1 - 布局规则.扩展余量比例));
  const 需要导轨设备 = 设备列表
    .filter((项目) => 项目.设备.安装方式 === "DIN导轨" || 项目.设备.安装方式 === "背板固定")
    .sort((甲, 乙) => {
      const 权重差 = 设备排序权重(甲.设备) - 设备排序权重(乙.设备);
      return 权重差 || 甲.设备.设备类别.localeCompare(乙.设备.设备类别, "zh-Hans-CN");
    });

  const 分组 = new Map<string, 已放置设备[]>();
  for (const 项目 of 需要导轨设备) {
    const 名称 = 分区名称(项目.设备);
    分组.set(名称, [...(分组.get(名称) ?? []), 项目]);
  }

  const 层列表: 导轨层[] = [];
  for (const [名称, 项目列表] of 分组.entries()) {
    let 当前层设备: 已放置设备[] = [];
    let 当前宽度 = 0;

    const 推入当前层 = () => {
      if (当前层设备.length === 0) return;
      层列表.push({
        层号: 层列表.length + 1,
        层名称: `第 ${层列表.length + 1} 层`,
        分区类型: 名称,
        设备列表: 当前层设备,
        占用宽度毫米: Math.round(当前宽度),
        可用宽度毫米: Math.round(每条导轨可用宽度),
        空间高度毫米: 计算导轨层空间高度(布局规则, 名称)
      });
      当前层设备 = [];
      当前宽度 = 0;
    };

    for (const 项目 of 项目列表) {
      const 设备宽度 = 计算实例占用宽度(项目) + 设备间距毫米;
      if (当前层设备.length > 0 && 当前宽度 + 设备宽度 > 可排布宽度) {
        推入当前层();
      }
      当前层设备.push(项目);
      当前宽度 += 设备宽度;
    }

    推入当前层();
  }

  return 层列表;
}

function 计算原始高度(层列表: 导轨层[], 有独立端子排: boolean, 布局规则: 布局规则) {
  const 有效层列表 = 层列表.length > 0
    ? 层列表
    : [{
        层号: 1,
        层名称: "第 1 层",
        分区类型: "空开 / 端子接线区",
        设备列表: [],
        占用宽度毫米: 0,
        可用宽度毫米: 0,
        空间高度毫米: 布局规则.空开端子区高度
      }];
  const 导轨高度 = 有效层列表.reduce((合计, 层) => 合计 + 层.空间高度毫米, 0);
  const 线槽高度 = Math.max(0, 有效层列表.length - 1) * 布局规则.横向线槽高度;
  const 端子排高度 = 有独立端子排 ? 布局规则.端子排区域高度 : 0;
  const 基础高度 =
    布局规则.顶部预留空间 +
    布局规则.底部预留空间 +
    导轨高度 +
    线槽高度 +
    端子排高度;

  return Math.ceil(基础高度 * (1 + 布局规则.扩展余量比例));
}

function 计算推荐深度(设备列表: 已放置设备[]) {
  const 最大设备深度 = Math.max(0, ...设备列表.map((项目) => 项目.设备.深度毫米));
  const 最大走线预留 = Math.max(40, ...设备列表.map((项目) => 项目.设备.走线预留毫米));
  const 原始深度 = Math.ceil((最大设备深度 + 最大走线预留) * 1.2);
  return {
    推荐深度: 选择候选规格(深度候选规格, 原始深度),
    原始深度
  };
}

function 计算每条导轨可用宽度(尺寸: 箱体尺寸, 布局规则: 布局规则) {
  return Math.max(1, 尺寸.宽度毫米 - 布局规则.左侧竖向线槽宽度 - 布局规则.右侧竖向线槽宽度 - 40);
}

function 计算自然最大导轨占用宽度(设备列表: 已放置设备[]) {
  const 需要导轨设备 = 设备列表
    .filter((项目) => 项目.设备.安装方式 === "DIN导轨" || 项目.设备.安装方式 === "背板固定")
    .sort((甲, 乙) => {
      const 权重差 = 设备排序权重(甲.设备) - 设备排序权重(乙.设备);
      return 权重差 || 甲.设备.设备类别.localeCompare(乙.设备.设备类别, "zh-Hans-CN");
    });

  const 分组宽度 = new Map<string, number>();
  for (const 项目 of 需要导轨设备) {
    const 名称 = 分区名称(项目.设备);
    const 宽度 = 计算实例占用宽度(项目) + 设备间距毫米;
    分组宽度.set(名称, (分组宽度.get(名称) ?? 0) + 宽度);
  }

  return Math.max(0, ...Array.from(分组宽度.values()));
}

function 计算当前导轨行宽度需求(设备列表: 已放置设备[], 布局规则: 布局规则) {
  const 导轨左起点 = 布局规则.左侧竖向线槽宽度 + 20;
  const 行映射 = new Map<number, { 左: number; 右: number }>();

  for (const 项目 of 设备列表) {
    if (!设备是否导轨安装(项目.设备) && 项目.设备.安装方式 !== "背板固定") continue;

    const 设备高度 = 计算实例显示高度(项目);
    const 设备中心Y = 项目.位置Y毫米 + 设备高度 / 2;
    const 分区类型 = 获取设备布局分区名称(项目.设备);
    const 估算层距 = 计算导轨层空间高度(布局规则, 分区类型) + 布局规则.横向线槽高度;
    const 层索引 = Math.max(0, Math.round((设备中心Y - 布局规则.顶部预留空间 - 计算导轨中心线距行顶(布局规则, 分区类型)) / 估算层距));
    const 左 = 项目.位置X毫米;
    const 右 = 项目.位置X毫米 + 计算实例占用宽度(项目);
    const 当前行 = 行映射.get(层索引);

    行映射.set(
      层索引,
      当前行
        ? {
            左: Math.min(当前行.左, 左),
            右: Math.max(当前行.右, 右)
          }
        : { 左, 右 }
    );
  }

  let 最大行内跨度 = 0;
  let 最大右侧需求 = 0;
  for (const 行 of 行映射.values()) {
    最大行内跨度 = Math.max(最大行内跨度, 行.右 - 行.左);
    最大右侧需求 = Math.max(最大右侧需求, 行.右 - 导轨左起点);
  }

  return Math.max(0, Math.ceil(Math.max(最大行内跨度, 最大右侧需求)));
}

function 计算标准推荐尺寸(设备列表: 已放置设备[], 布局规则: 布局规则) {
  const 有独立端子排 = 设备列表.some((项目) => 项目.设备.安装方式 === "端子排");
  const { 推荐深度, 原始深度 } = 计算推荐深度(设备列表);
  const 自然最大导轨占用宽度 = 计算自然最大导轨占用宽度(设备列表);
  const 当前手动导轨宽度需求 = 计算当前导轨行宽度需求(设备列表, 布局规则);
  const 推荐导轨宽度需求 = Math.max(自然最大导轨占用宽度, 当前手动导轨宽度需求);
  const 推荐宽度需求 =
    布局规则.左侧竖向线槽宽度 +
    布局规则.右侧竖向线槽宽度 +
    40 +
    Math.ceil(推荐导轨宽度需求 * (1 + 布局规则.扩展余量比例));
  const 最小推荐宽度 = 选择候选规格(宽度候选规格, Math.max(400, 推荐宽度需求));
  let 标准推荐箱体尺寸: 箱体尺寸 = { 宽度毫米: 400, 高度毫米: 500, 深度毫米: 推荐深度 };
  let 标准推荐层: 导轨层[] = [];

  for (const 宽度 of 宽度候选规格) {
    if (宽度 < 最小推荐宽度) continue;
    const 预估尺寸 = { 宽度毫米: 宽度, 高度毫米: 500, 深度毫米: 推荐深度 };
    const 每条导轨可用宽度 = 计算每条导轨可用宽度(预估尺寸, 布局规则);
    const 层列表 = 规划导轨层(设备列表, 每条导轨可用宽度, 布局规则);
    const 原始高度 = 计算原始高度(层列表, 有独立端子排, 布局规则);
    const 高度 = 选择候选规格(高度候选规格, 原始高度);

    标准推荐箱体尺寸 = { 宽度毫米: 宽度, 高度毫米: 高度, 深度毫米: 推荐深度 };
    标准推荐层 = 层列表;
    if (高度 >= 原始高度) break;
  }

  return {
    标准推荐箱体尺寸,
    标准推荐层,
    原始深度
  };
}

function 生成设备清单(设备列表: 已放置设备[]): 设备清单项[] {
  const 清单 = new Map<string, 设备清单项>();

  for (const 项目 of 设备列表) {
    const 设备 = 项目.设备;
    const 现有 = 清单.get(设备.设备编号);
    if (现有) {
      现有.数量 += 获取实例数量(项目);
      现有.总发热功率瓦 += 设备.发热功率瓦 * 获取实例数量(项目);
      continue;
    }

    清单.set(设备.设备编号, {
      设备编号: 设备.设备编号,
      设备名称: 设备.设备名称,
      品牌: 设备.品牌,
      设备类别: 设备.设备类别,
      设备类型: 设备.设备类型,
      安装方式: 设备.安装方式,
      数量: 获取实例数量(项目),
      单台宽度毫米: 设备.宽度毫米,
      单台高度毫米: 设备.高度毫米,
      单台深度毫米: 设备.深度毫米,
      总发热功率瓦: 设备.发热功率瓦 * 获取实例数量(项目),
      备注: 设备.备注
    });
  }

  return Array.from(清单.values());
}

