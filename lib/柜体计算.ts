function 计算散热建议(总发热功率: number) {
  if (总发热功率 >= 120) {
    return {
      推荐散热等级: "高",
      散热建议: "建议加大箱体、上下通风百叶，并配置强制散热风扇。"
    };
  }
  if (总发热功率 >= 80) {
    return {
      推荐散热等级: "中高",
      散热建议: "建议配置散热孔或风扇，发热设备与控制模块保持间距。"
    };
  }
  if (总发热功率 >= 40) {
    return {
      推荐散热等级: "中",
      散热建议: "建议预留通风孔，并避免电源、调光器紧贴弱电控制器。"
    };
  }
  return {
    推荐散热等级: "低",
    散热建议: "自然散热基本可满足，仍需按现场环境确认通风。"
  };
}

export function 计算推荐方案(设备列表: 已放置设备[], 箱体配置: 箱体配置 = 默认箱体配置): 计算结果 {
  const 布局规则 = 合并布局规则(箱体配置);
  const 总设备数量 = 设备列表.length;
  const 总发热功率 = 设备列表.reduce((合计, 项目) => 合计 + 项目.设备.发热功率瓦 * 获取实例数量(项目), 0);
  const 有独立端子排 = 设备列表.some((项目) => 项目.设备.安装方式 === "端子排");
  const { 标准推荐箱体尺寸, 原始深度 } = 计算标准推荐尺寸(设备列表, 布局规则);
  const 推荐尺寸 =
    箱体配置.尺寸模式 === "自定义尺寸" ? { ...箱体配置.自定义箱体尺寸 } : { ...标准推荐箱体尺寸 };

  const 每条导轨可用宽度 = 计算每条导轨可用宽度(推荐尺寸, 布局规则);
  const 推荐层 = 规划导轨层(设备列表, 每条导轨可用宽度, 布局规则);
  const 导轨数量 = Math.max(1, 推荐层.length);
  const 高度需求 = 计算原始高度(推荐层, 有独立端子排, 布局规则);

  const 设备面积 = 设备列表.reduce(
    (合计, 项目) => 合计 + 计算实例占用宽度(项目) * 计算实例显示高度(项目),
    0
  );
  const 可用面积 = Math.max(
    1,
    每条导轨可用宽度 * (推荐尺寸.高度毫米 - 布局规则.顶部预留空间 - 布局规则.底部预留空间)
  );
  const 空间利用率 = Math.min(100, Math.round((设备面积 / 可用面积) * 100));
  const 当前手动导轨宽度需求 = 计算当前导轨行宽度需求(设备列表, 布局规则);
  const 宽度最高占用 = Math.max(当前手动导轨宽度需求, ...推荐层.map((层) => 层.占用宽度毫米));
  const 宽度余量 = Math.max(0, Math.round(((每条导轨可用宽度 - 宽度最高占用) / 每条导轨可用宽度) * 100));
  const 高度余量 = Math.max(0, Math.round(((推荐尺寸.高度毫米 - 高度需求) / 推荐尺寸.高度毫米) * 100));

  const 警告信息: string[] = [];
  if (设备列表.length === 0) {
    警告信息.push("当前项目还没有放入设备，推荐尺寸按最小箱体显示。");
  }
  if (原始深度 > 深度候选规格[深度候选规格.length - 1]) {
    警告信息.push("部分设备深度和走线预留超过 600 毫米候选深度，需要定制更深箱体。");
  }
  if (推荐尺寸.宽度毫米 < 标准推荐箱体尺寸.宽度毫米 && 设备列表.length > 0) {
    警告信息.push("当前自定义宽度小于标准推荐宽度，可能需要减少同层设备或增加导轨层。");
  }
  if (推荐尺寸.高度毫米 < 高度需求 && 设备列表.length > 0) {
    警告信息.push("当前自定义高度小于按行距和线槽计算的需求高度，设备可能超出箱体。");
  }
  if (推荐尺寸.深度毫米 < 标准推荐箱体尺寸.深度毫米 && 设备列表.length > 0) {
    警告信息.push("当前自定义深度小于标准推荐深度，请检查最深设备和走线空间。");
  }
  if (每条导轨可用宽度 < 120) {
    警告信息.push("左右线槽或箱体宽度设置过大，DIN 导轨可用宽度低于 120 毫米。");
  }
  for (const 项目 of 设备列表) {
    if (项目.设备.深度毫米 > 推荐尺寸.深度毫米) {
      警告信息.push(`${项目.设备.设备名称} 的设备深度超过当前箱体深度。`);
    }
    if (项目.设备.安装方式 === "机柜U位") {
      警告信息.push(`${项目.设备.设备名称} 属于机柜 U 位设备，第一版仅保留网络机柜数据结构。`);
    }
  }
  if (总发热功率 >= 80) {
    警告信息.push("总发热功率较高，建议增加散热孔、风扇或选择更大箱体。");
  }
  if (宽度余量 < 15 && 设备列表.length > 0) {
    警告信息.push("导轨横向余量低于 15%，建议选择更宽箱体或减少同层设备。");
  }
  if (高度余量 < 10 && 设备列表.length > 0) {
    警告信息.push("高度余量低于 10%，后续扩展可能受限。");
  }

  const 散热 = 计算散热建议(总发热功率);
  const 设备清单 = 生成设备清单(设备列表);
  const 材料清单 = 生成材料清单(设备列表, 推荐尺寸, 导轨数量, 总发热功率, 布局规则);

  return {
    推荐箱体尺寸: 推荐尺寸,
    标准推荐箱体尺寸,
    推荐箱体尺寸文本: 格式化尺寸(推荐尺寸),
    标准推荐箱体尺寸文本: 格式化尺寸(标准推荐箱体尺寸),
    箱体尺寸来源: 箱体配置.尺寸模式,
    DIN导轨数量: 导轨数量,
    每条导轨可用宽度: Math.round(每条导轨可用宽度),
    横向线槽高度: 布局规则.横向线槽高度,
    纵向线槽宽度: Math.max(布局规则.左侧竖向线槽宽度, 布局规则.右侧竖向线槽宽度),
    顶部预留空间: 布局规则.顶部预留空间,
    底部预留空间: 布局规则.底部预留空间,
    总设备数量,
    总发热功率,
    推荐散热等级: 散热.推荐散热等级,
    散热建议: 散热.散热建议,
    空间利用率,
    扩展余量: `宽度余量 ${宽度余量}% / 高度余量 ${高度余量}%`,
    每层设备分布: 推荐层,
    线槽空间占用情况: [
      `左侧竖向线槽 ${布局规则.左侧竖向线槽宽度} 毫米`,
      `右侧竖向线槽 ${布局规则.右侧竖向线槽宽度} 毫米`,
      `横向线槽宽度 ${布局规则.横向线槽高度} 毫米`,
      `空开 / 端子接线区 ${布局规则.空开端子区高度} 毫米`,
      `模块区 ${布局规则.模块区高度} 毫米`,
      `顶部预留 ${布局规则.顶部预留空间} 毫米`,
      `底部预留 ${布局规则.底部预留空间} 毫米`,
      有独立端子排
        ? `独立端子端口区 ${布局规则.端子排区域高度} 毫米`
        : `端子默认与空开同排，按 ${布局规则.空开端子区高度} 毫米接线区计算`
    ],
    设备清单,
    材料清单,
    警告信息
  };
}

export function 生成自动布局(设备列表: 已放置设备[], 箱体配置: 箱体配置 = 默认箱体配置) {
  const 布局规则 = 合并布局规则(箱体配置);
  const 初始结果 = 计算推荐方案(设备列表, 箱体配置);
  const 每条导轨可用宽度 = 初始结果.每条导轨可用宽度;
  const 左起点 = 布局规则.左侧竖向线槽宽度 + 24;
  const 分区类型列表 = 初始结果.每层设备分布.map((层) => 层.分区类型);
  const 更新映射 = new Map<string, 已放置设备>();

  for (const 层 of 初始结果.每层设备分布) {
    let 当前X = 左起点;
    for (const 项目 of 层.设备列表) {
      const 层Y = 设备是否导轨安装(项目.设备)
        ? 计算导轨安装顶部Y(项目.设备, 布局规则, 层.层号 - 1, 分区类型列表)
        : 计算导轨层顶部Y(布局规则, 层.层号 - 1, 分区类型列表) + 20;
      更新映射.set(项目.实例编号, {
        ...项目,
        位置X毫米: 当前X,
        位置Y毫米: 层Y,
        所在层: 层.层号,
        布局来源: "自动排布"
      });
      当前X += 计算实例占用宽度(项目) + 设备间距毫米;
    }
  }

  const 端子设备 = 设备列表.filter((项目) => 项目.设备.安装方式 === "端子排");
  let 端子X = 左起点;
  const 导轨区底部Y = 初始结果.每层设备分布.reduce(
    (当前Y, 层, 索引) => 当前Y + 层.空间高度毫米 + (索引 < 初始结果.每层设备分布.length - 1 ? 布局规则.横向线槽高度 : 0),
    布局规则.顶部预留空间
  );
  const 端子Y = Math.max(
    导轨区底部Y + 布局规则.横向线槽高度,
    初始结果.推荐箱体尺寸.高度毫米 - 布局规则.底部预留空间 - 布局规则.端子排区域高度 + 28
  );
  for (const 项目 of 端子设备) {
    更新映射.set(项目.实例编号, {
      ...项目,
      位置X毫米: 端子X,
      位置Y毫米: 端子Y,
      所在层: 初始结果.DIN导轨数量 + 1,
      布局来源: "自动排布"
    });
    端子X += Math.min(计算实例占用宽度(项目) + 设备间距毫米, 每条导轨可用宽度 / 2);
  }

  const 新设备列表 = 设备列表.map((项目) => 更新映射.get(项目.实例编号) ?? 项目);
  return {
    设备列表: 新设备列表,
    计算结果: 计算推荐方案(新设备列表, 箱体配置)
  };
}
