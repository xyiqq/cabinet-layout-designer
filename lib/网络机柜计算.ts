import type {
  U位占用项,
  已放置网络设备,
  网络机柜规格,
  网络机柜计算结果,
  网络材料清单项,
  网络设备参数,
  网络设备清单项
} from "@/types/设备";

const 扩展预留比例 = 0.2;
const 常用深度 = [450, 600, 800, 1000];
const 标准网络机柜宽度毫米 = 600;
const 标准网络机柜深度毫米 = 600;

export function 计算网络设备占用U数(设备: 网络设备参数) {
  if (设备.安装方式 === "后侧安装" || 设备.安装方式 === "箱内DIN导轨") return 0;
  return Math.max(1, Math.ceil(设备.机柜U数 || 1));
}

export function 创建网络设备实例(设备: 网络设备参数, 起始U位 = 1): 已放置网络设备 {
  return {
    实例编号: `${设备.设备编号}-${Date.now()}-${Math.round(Math.random() * 10000)}`,
    设备,
    起始U位,
    占用U数: 计算网络设备占用U数(设备),
    布局来源: "手动拖拽"
  };
}

function 网络设备排序权重(设备: 网络设备参数) {
  const 类别 = 设备.设备类别;
  const 类型 = 设备.设备类型;
  if (设备.安装方式 === "后侧安装" || 类别.includes("PDU")) return 95;
  if (类别.includes("防火墙")) return 10;
  if (类别.includes("路由器")) return 12;
  if (类别.includes("光猫")) return 14;
  if (类别.includes("无线AP控制器")) return 16;
  if (类别.includes("智能家居网关")) return 18;
  if (类别.includes("配线架")) return 24;
  if (类别.includes("理线架")) return 25;
  if (类别.includes("核心交换机")) return 28;
  if (类别.includes("PoE交换机")) return 30;
  if (类型.includes("交换机")) return 32;
  if (类别.includes("托盘设备")) return 48;
  if (类别.includes("NVR")) return 60;
  if (类别.includes("NAS")) return 66;
  if (类别.includes("机柜配电箱")) return 72;
  if (类别.includes("UPS")) return 90;
  if (类别.includes("配电箱内置组件")) return 94;
  if (类别.includes("盲板")) return 100;
  return 50 + (设备.是否重设备 === "是" ? 20 : 0);
}

function 计算显式理线架U数(设备列表: 已放置网络设备[]) {
  return 设备列表
    .filter((项目) => 项目.设备.设备类别.includes("理线架"))
    .reduce((合计, 项目) => 合计 + 计算网络设备占用U数(项目.设备), 0);
}

function 计算理线架需求U数(设备列表: 已放置网络设备[], 显式理线架U数 = 计算显式理线架U数(设备列表)) {
  const 总网口 = 设备列表.reduce((合计, 项目) => 合计 + 项目.设备.网口数量, 0);
  const 有理线架需求 = 设备列表.some(
    (项目) => 项目.设备.是否需要理线架 === "是" || 项目.设备.设备类别.includes("配线架") || 项目.设备.设备类别.includes("理线架")
  );
  return Math.max(显式理线架U数, 有理线架需求 && 总网口 > 0 ? 1 : 0);
}

function 选择候选值(候选值: number[], 需求值: number) {
  return 候选值.find((值) => 值 >= 需求值) ?? 候选值[候选值.length - 1];
}

function 推荐机柜宽度() {
  return 标准网络机柜宽度毫米;
}

function 推荐机柜深度(设备列表: 已放置网络设备[]) {
  const 深度需求 = Math.max(
    标准网络机柜深度毫米,
    ...设备列表
      .filter((项目) => 计算网络设备占用U数(项目.设备) > 0)
      .map((项目) => 项目.设备.深度毫米 + 项目.设备.走线预留毫米)
  );
  return 选择候选值(常用深度, 深度需求);
}

function 生成设备清单(设备列表: 已放置网络设备[]): 网络设备清单项[] {
  const 清单 = new Map<string, 网络设备清单项 & { 数量: number }>();
  const 设备名称索引 = new Map(设备列表.map((项目) => [项目.实例编号, 项目.设备.设备名称]));
  for (const 项目 of 设备列表) {
    const 设备 = 项目.设备;
    const 现有 = 清单.get(设备.设备编号);
    if (现有) {
      现有.数量 += 1;
      现有.总发热功率瓦 += 设备.发热功率瓦;
      continue;
    }

    清单.set(设备.设备编号, {
      设备编号: 设备.设备编号,
      设备名称: 设备.设备名称,
      品牌: 设备.品牌,
      设备类别: 设备.设备类别,
      设备类型: 设备.设备类型,
      安装方式: 设备.安装方式,
      机柜U数: 计算网络设备占用U数(设备),
      深度毫米: 设备.深度毫米,
      网口数量: 设备.网口数量,
      PoE口数量: 设备.PoE口数量,
      总发热功率瓦: 设备.发热功率瓦,
      备注:
        设备.安装方式 === "箱内DIN导轨" && 项目.所属配电箱实例编号
          ? `${设备.备注}；放入：${设备名称索引.get(项目.所属配电箱实例编号) ?? "未命名配电箱"}`
          : 设备.备注,
      数量: 1
    });
  }

  return Array.from(清单.values()).map(({ 数量, ...项目 }) => ({
    ...项目,
    设备名称: 数量 > 1 ? `${项目.设备名称} ×${数量}` : 项目.设备名称
  }));
}

function 生成U位占用表(设备列表: 已放置网络设备[]) {
  const 占用表: U位占用项[] = [];

  for (const 项目 of 设备列表) {
    const 占用U数 = 计算网络设备占用U数(项目.设备);
    if (占用U数 <= 0) continue;

    const 起始U位 = Math.max(1, Math.round(项目.起始U位 || 1));
    占用表.push({
      起始U位,
      结束U位: 起始U位 + 占用U数 - 1,
      占用U数,
      名称: 项目.设备.设备名称,
      类型: 项目.设备.设备类别,
      设备编号: 项目.设备.设备编号,
      实例编号: 项目.实例编号,
      是否自动生成: "否"
    });
  }

  const 显式理线架U数 = 计算显式理线架U数(设备列表);
  const 自动理线架U数 = Math.max(0, 计算理线架需求U数(设备列表, 显式理线架U数) - 显式理线架U数);
  const 理线架锚点 = [...设备列表]
    .filter((项目) => 计算网络设备占用U数(项目.设备) > 0)
    .sort((甲, 乙) => Math.round(甲.起始U位 || 1) - Math.round(乙.起始U位 || 1))
    .find((项目) => 项目.设备.设备类别.includes("配线架")) ??
    [...设备列表]
      .filter((项目) => 计算网络设备占用U数(项目.设备) > 0)
      .sort((甲, 乙) => Math.round(甲.起始U位 || 1) - Math.round(乙.起始U位 || 1))
      .find((项目) => 项目.设备.是否需要理线架 === "是");

  const 区间是否可用 = (起始U位: number, 占用U数: number) => {
    const 结束U位 = 起始U位 + 占用U数 - 1;
    return !占用表.some((项目) => 起始U位 <= 项目.结束U位 && 结束U位 >= 项目.起始U位);
  };

  const 查找最近空U位 = (期望U位: number) => {
    const 最大结束U位 = Math.max(1, ...占用表.map((项目) => 项目.结束U位));
    const 最大搜索U位 = Math.max(期望U位, 最大结束U位) + 80;
    const 起点 = Math.max(1, Math.round(期望U位));
    if (区间是否可用(起点, 1)) return 起点;

    for (let 偏移 = 1; 偏移 <= 最大搜索U位; 偏移 += 1) {
      const 向下 = 起点 + 偏移;
      const 向上 = 起点 - 偏移;
      if (区间是否可用(向下, 1)) return 向下;
      if (向上 >= 1 && 区间是否可用(向上, 1)) return 向上;
    }

    return 最大结束U位 + 1;
  };

  if (自动理线架U数 > 0) {
    const 锚点起始U位 = Math.max(1, Math.round(理线架锚点?.起始U位 || 1));
    const 锚点占用U数 = 理线架锚点 ? 计算网络设备占用U数(理线架锚点.设备) : 1;
    const 期望起始U位 = 理线架锚点?.设备.设备类别.includes("配线架")
      ? 锚点起始U位 + 锚点占用U数
      : Math.max(1, 锚点起始U位 - 自动理线架U数);

    for (let 索引 = 0; 索引 < 自动理线架U数; 索引 += 1) {
      const 起始U位 = 查找最近空U位(期望起始U位 + 索引);
      占用表.push({
        起始U位,
        结束U位: 起始U位,
        占用U数: 1,
        名称: "优诺 1U 金属理线架",
        类型: "理线架",
        实例编号: `${理线架锚点?.实例编号 ?? "自动"}-理线架预留-${索引 + 1}`,
        是否自动生成: "是"
      });
    }
  }

  return 占用表.sort((甲, 乙) => 甲.起始U位 - 乙.起始U位 || 甲.结束U位 - 乙.结束U位);
}

function 选择推荐规格(规格库: 网络机柜规格[], 推荐U数: number, 推荐宽度: number, 推荐深度: number) {
  const 合适规格 = 规格库
    .filter((规格) => 规格.机柜U数 >= 推荐U数 && 规格.宽度毫米 >= 推荐宽度 && 规格.深度毫米 >= 推荐深度)
    .sort(
      (甲, 乙) =>
        甲.机柜U数 - 乙.机柜U数 ||
        甲.宽度毫米 - 乙.宽度毫米 ||
        甲.深度毫米 - 乙.深度毫米 ||
        甲.高度毫米 - 乙.高度毫米
    );

  return (
    合适规格[0] ??
    [...规格库].sort(
      (甲, 乙) =>
        乙.机柜U数 - 甲.机柜U数 ||
        乙.宽度毫米 - 甲.宽度毫米 ||
        乙.深度毫米 - 甲.深度毫米
    )[0]
  );
}

