function 计算散热建议(总发热功率: number) {
  if (总发热功率 >= 220) return "总发热功率很高，建议选择深柜、顶部风扇和通风盲板；系统不会额外占用U位。";
  if (总发热功率 >= 160) return "建议配置主动散热风扇，并确认前后门通风；设备高度仍按自身U数计算。";
  if (总发热功率 >= 90) return "建议保持前后门通风，必要时使用通风盲板或风扇，不额外生成空U位。";
  return "自然散热基本可满足，仍需按机房温度和设备负载确认通风。";
}

export function 计算网络机柜方案(
  设备列表: 已放置网络设备[],
  规格库: 网络机柜规格[]
): 网络机柜计算结果 {
  const 最大常用机柜U数 = Math.max(0, ...规格库.map((规格) => 规格.机柜U数));
  const 设备总U数 = 设备列表.reduce((合计, 项目) => 合计 + 计算网络设备占用U数(项目.设备), 0);
  const 托盘占用U数 = 设备列表
    .filter((项目) => 项目.设备.是否需要托盘 === "是" || 项目.设备.设备类别.includes("托盘"))
    .reduce((合计, 项目) => 合计 + 计算网络设备占用U数(项目.设备), 0);
  const 盲板占用U数 = 设备列表
    .filter((项目) => 项目.设备.设备类别.includes("盲板"))
    .reduce((合计, 项目) => 合计 + 计算网络设备占用U数(项目.设备), 0);
  const U位占用表 = 生成U位占用表(设备列表);
  const 理线架占用U数 = U位占用表
    .filter((项目) => 项目.类型.includes("理线架"))
    .reduce((合计, 项目) => 合计 + 项目.占用U数, 0);
  const 实际占用U数 = U位占用表.reduce((合计, 项目) => 合计 + 项目.占用U数, 0);
  const 最大结束U位 = Math.max(0, ...U位占用表.map((项目) => 项目.结束U位));
  const 推荐机柜U数需求 = Math.max(1, Math.ceil(Math.max(实际占用U数, 最大结束U位) / (1 - 扩展预留比例)));
  const 推荐U数 = 选择候选值([...new Set(规格库.map((规格) => 规格.机柜U数))].sort((甲, 乙) => 甲 - 乙), 推荐机柜U数需求);
  const 推荐宽度 = 推荐机柜宽度();
  const 推荐深度 = 推荐机柜深度(设备列表);
  const 推荐机柜规格 = 选择推荐规格(规格库, 推荐U数, 推荐宽度, 推荐深度);
  const 已用U基准 = Math.max(实际占用U数, 最大结束U位);
  const U位利用率 = 推荐机柜规格 ? Math.min(100, Math.round((已用U基准 / 推荐机柜规格.机柜U数) * 100)) : 0;
  const 扩展U数 = Math.max(0, 推荐机柜规格.机柜U数 - 已用U基准);
  const 扩展百分比 = 推荐机柜规格.机柜U数 > 0 ? Math.round((扩展U数 / 推荐机柜规格.机柜U数) * 100) : 0;
  const 总发热功率 = 设备列表.reduce((合计, 项目) => 合计 + 项目.设备.发热功率瓦, 0);
  const 推荐机柜类型 = 推荐机柜规格.是否壁挂机柜 === "是" ? "壁挂机柜" : "落地机柜";
  const 设备清单 = 生成设备清单(设备列表);

  const 警告信息: string[] = [];
  if (设备列表.length === 0) 警告信息.push("当前网络机柜还没有放入设备，推荐规格按最小常用机柜显示。");
  if (最大常用机柜U数 > 0 && 已用U基准 > 最大常用机柜U数) {
    警告信息.push(
      `当前设备过多，U位顺延后已占用到 ${已用U基准}U，超过常用最大 ${最大常用机柜U数}U，需要更高机柜、拆分机柜或删除设备。`
    );
  } else if (最大常用机柜U数 > 0 && 推荐机柜U数需求 > 最大常用机柜U数) {
    警告信息.push(`按 20% 扩展余量计算需要 ${推荐机柜U数需求}U，已超过常用最大 ${最大常用机柜U数}U，建议拆分机柜或减少设备。`);
  }
  if (已用U基准 > 推荐机柜规格.机柜U数) {
    警告信息.push(`当前U位占用达到 ${已用U基准}U，超过推荐机柜 ${推荐机柜规格.机柜U数}U。`);
  }
  if (扩展百分比 < 20 && 设备列表.length > 0) 警告信息.push("U位扩展空间低于 20%，建议选择更高U数机柜。");
  const 机柜配电箱列表 = 设备列表.filter((项目) => 项目.设备.设备类别.includes("机柜配电箱"));
  const 机柜配电箱数量 = 机柜配电箱列表.length;
  const 机柜配电箱实例编号 = new Set(机柜配电箱列表.map((项目) => 项目.实例编号));
  const 箱内DIN组件 = 设备列表.filter(
    (项目) => 项目.设备.安装方式 === "箱内DIN导轨" || 项目.设备.设备类别.includes("配电箱内置组件")
  );
  const 未归属箱内DIN组件 = 箱内DIN组件.filter(
    (项目) => !项目.所属配电箱实例编号 || !机柜配电箱实例编号.has(项目.所属配电箱实例编号)
  );
  if (箱内DIN组件.length > 0 && 机柜配电箱数量 === 0) {
    警告信息.push("已添加箱内DIN组件，但当前机柜内没有 3U 机柜配电箱，请先加入配电箱箱体或改用预装组合。");
  } else if (未归属箱内DIN组件.length > 0) {
    警告信息.push(`有 ${未归属箱内DIN组件.length} 个箱内DIN组件还没有放入指定 3U 机柜配电箱。`);
  }
  for (const 配电箱 of 机柜配电箱列表) {
    const 箱内DIN模数 = 箱内DIN组件
      .filter((项目) => 项目.所属配电箱实例编号 === 配电箱.实例编号)
      .reduce((合计, 项目) => 合计 + Math.max(1, Math.ceil(项目.设备.宽度毫米 / 18)), 0);
    if (箱内DIN模数 > 24) {
      警告信息.push(`${配电箱.设备.设备名称} 内DIN组件约占 ${箱内DIN模数} 模数，超过单台 3U 配电箱估算容量 24 模数。`);
    }
  }
  for (const 项目 of 设备列表) {
    if (计算网络设备占用U数(项目.设备) <= 0) continue;
    if (项目.设备.深度毫米 > 推荐机柜规格.深度毫米) {
      警告信息.push(`${项目.设备.设备名称} 的设备深度 ${项目.设备.深度毫米} 毫米超过机柜深度 ${推荐机柜规格.深度毫米} 毫米。`);
    } else if (项目.设备.深度毫米 + 项目.设备.走线预留毫米 > 推荐机柜规格.深度毫米) {
      警告信息.push(`${项目.设备.设备名称} 加走线预留后超过当前机柜深度，建议选择更深机柜或优化后侧走线。`);
    }
  }
  if (总发热功率 >= 160) 警告信息.push("总发热功率较高，建议配置风扇、通风盲板或选择更深机柜。");

  const 材料清单 = 生成材料清单(设备列表, {
    推荐机柜规格,
    理线架占用U数,
    托盘占用U数,
    盲板占用U数,
    总发热功率
  });

  return {
    设备总U数,
    理线架占用U数,
    托盘占用U数,
    盲板占用U数,
    推荐机柜U数: 推荐机柜规格.机柜U数,
    推荐机柜宽度: 推荐机柜规格.宽度毫米,
    推荐机柜深度: 推荐机柜规格.深度毫米,
    推荐机柜类型,
    U位利用率,
    扩展余量: `${扩展U数}U / ${扩展百分比}%`,
    总发热功率,
    散热建议: 计算散热建议(总发热功率),
    推荐机柜规格,
    设备清单,
    材料清单,
    U位占用表,
    警告信息
  };
}

export function 生成网络机柜自动布局(
  设备列表: 已放置网络设备[],
  规格库: 网络机柜规格[]
) {
  let 当前U位 = 1;
  const 有配线架 = 设备列表.some((项目) => 项目.设备.设备类别.includes("配线架"));
  const 显式理线架U数 = 计算显式理线架U数(设备列表);
  let 待自动预留理线架U数 = Math.max(0, 计算理线架需求U数(设备列表, 显式理线架U数) - 显式理线架U数);
  const 更新后设备列表 = [...设备列表]
    .sort((甲, 乙) => {
      const 权重差 = 网络设备排序权重(甲.设备) - 网络设备排序权重(乙.设备);
      return 权重差 || 甲.设备.设备名称.localeCompare(乙.设备.设备名称, "zh-Hans-CN");
    })
    .map((项目) => {
      const 占用U数 = 计算网络设备占用U数(项目.设备);
      if (占用U数 <= 0) {
        return { ...项目, 起始U位: 0, 占用U数, 布局来源: "自动排布" as const };
      }

      if (!有配线架 && 待自动预留理线架U数 > 0 && 项目.设备.是否需要理线架 === "是") {
        当前U位 += 待自动预留理线架U数;
        待自动预留理线架U数 = 0;
      }

      const 起始U位 = 当前U位;
      当前U位 += 占用U数;
      if (有配线架 && 待自动预留理线架U数 > 0 && 项目.设备.设备类别.includes("配线架")) {
        当前U位 += 待自动预留理线架U数;
        待自动预留理线架U数 = 0;
      }
      return {
        ...项目,
        起始U位,
        占用U数,
        布局来源: "自动排布" as const
      };
    });

  return {
    设备列表: 更新后设备列表,
    计算结果: 计算网络机柜方案(更新后设备列表, 规格库)
  };
}
