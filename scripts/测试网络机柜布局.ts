function 创建设备(
  设备编号: string,
  机柜U数: number,
  安装方式: 网络设备参数["安装方式"] = "机柜U位",
  设备类别 = "测试设备"
): 网络设备参数 {
  return {
    设备编号,
    设备名称: 设备编号,
    品牌: "测试",
    设备类别,
    设备类型: "测试设备",
    安装方式,
    宽度毫米: 482,
    高度毫米: 44.45 * Math.max(1, 机柜U数),
    深度毫米: 300,
    机柜U数,
    发热功率瓦: 10,
    走线预留毫米: 100,
    供电类型: "AC",
    网口数量: 0,
    PoE口数量: 0,
    是否重设备: "否",
    是否需要托盘: "否",
    是否需要理线架: "否",
    是否需要散热间距: "否",
    备注: ""
  };
}

function 创建实例(
  实例编号: string,
  起始U位: number,
  机柜U数 = 1,
  安装方式: 网络设备参数["安装方式"] = "机柜U位",
  设备类别 = "测试设备"
): 已放置网络设备 {
  const 设备 = 创建设备(实例编号, 机柜U数, 安装方式, 设备类别);
  return {
    实例编号,
    设备,
    起始U位,
    占用U数: 计算网络设备占用U数(设备),
    布局来源: "自动排布"
  };
}

function 起始U位映射(设备列表: 已放置网络设备[]) {
  return Object.fromEntries(设备列表.map((项目) => [项目.实例编号, 项目.起始U位]));
}

function 实例顺序(设备列表: 已放置网络设备[]) {
  return 设备列表.map((项目) => 项目.实例编号);
}

